package aula01;

public class If_Else {
	public static void main(String[] args) {
		int numero = 2;
		if(numero > 0) { //entao faca
			System.out.println("Número positivo");
		}else if(numero < 0) {
			System.out.println("Número negativo");
		}else {
			System.out.println("Número zero");
		}//fim else
	}//fim main
}//fim classe
